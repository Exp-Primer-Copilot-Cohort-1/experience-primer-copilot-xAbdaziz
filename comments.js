// Create web server
const express = require('express');
const app = express();

// Create a route
app.get('/comment', (req, res) => {
    res.send('Hello World');
});

// Start the server
app.listen(3000, () => console.log('Listening on port 3000'));

// Run the file
// node comment.js

// Go to the browser
// http://localhost:3000/comment
// Hello World

// Go to the terminal and press Ctrl+C to stop the server

